package be.pxl.h1.opdracht4;

public class opdracht4 {
    public static void main(String[] args) {
        int i;
        int n = 3;
        double d;
        i = n + 4; // 7
        System.out.println(i);
        i = n * 4; // 12
        System.out.println(i);
        i = 7 + 2 * 3; // 13
        System.out.println(i);
        n = n * (n + 2) * 4; // 20
        System.out.println(n);
        d = 3.5 / 2; // 1.75
        System.out.println(d);
        n = 7 / 4; // 1
        System.out.println(n);
        d = 7 / 4;
        System.out.println(d);
    }

}
